#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ptr;

	ptr = (char *)s;
	while (*ptr != '\0' && *ptr != c)
		ptr++;
	return (*ptr == c) ? ptr : NULL;
}
